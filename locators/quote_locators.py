class QuoteLocators:
    """A class that contains the quote CSS locators.

    Parameters
    ----------
    AUTHOR : str
        The CSS locator that selects the span tag of class author.
    CONTENT : str
        The CSS locator that selects the span tag of class content.
    TAGS : str
        The CSS locator that selects the span tag of class tag.
    """
    AUTHOR = "span.author"
    CONTENT = "span.content"
    TAGS = "span.tag"
